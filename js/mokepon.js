function traducirAEmoji(resultado){
    switch(resultado){
        case "EMPATE": return '‚ûñ'; break;
        case "GANASTE": return '‚úî'; break;
        case "PERDISTE": return '‚ùå'; break;
        default: return resultado; break;
        }
}

